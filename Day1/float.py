#float variables
num1 = 4.0
num2 = 3.0

print(num1 + num2)

#Addition operations
result1 = num1 + num2
print("Addition: ", result1)

#Subtraction operations
result2 = num1 - num2
print("Subtraction: ", result2)

#Multiplication operations
result3 = num1 * num2
print("Multiplication: ", result3)

#Division operations
result4 = num1 / num2
print("Division: ", result4)

#Exponent operations
result6 = num1 ** num2
print("Exponent: ", result6)

#Floor division operations
result7 = num1 // num2
print("Floor division: ", result7)

#Round operations
result8 = round(result4, 3)
print("Round division: ", result8)  #round to 3 d
