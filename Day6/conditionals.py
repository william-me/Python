#Elif statement    
x = 10
if x > 15:
    print("x is greater than 15")
elif x > 5:
    print("x is greater than 5 but not greater than 15")
else:
    print("x is not greater than 5")
 
    
#Else statement
x = 3
if x > 5:
    print("x is greater than 5")
else:
    print("x is not greater than 5")